package asssignmentproblem;

public class Sumoffifteennum {
	public static void main(String[] args) {
		
		//WAP to calculate the sum of first 15 number using for loop.

		
		int sum=0;
		int i=1;
		
		for(i=1;i<=15;i++) {
			sum = sum+i;
			
		}
		System.out.println("Sum of first 15 numbers using for loop:-->" +sum);
	
	}

}
