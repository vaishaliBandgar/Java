package Logicalprog;

public class Swappingtwono {
	
	public static void main(String[] args) {
		 int a=10, b=20;
		 
			System.out.println("Before swapping values :"+a+" "+b);
			
			int c=a;  //10
			    a=b;     //20
			    b=c;   //   10
			    System.out.println("After swapping values :"+a+" "+b);
	}    
}