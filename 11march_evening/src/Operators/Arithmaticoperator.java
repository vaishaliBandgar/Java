package Operators;

public class Arithmaticoperator {
	
	public static void main(String[] args) {
		
		int a= 50;
		int b =20;
		
		
		System.out.println(a+b);  //70
		System.out.println(a-b);  //30
		System.out.println(a*b); // 1000
		System.out.println(a/b); //2
		System.out.println(a%b); //10
		
	}

}
