package Operators;

public class Retionaloperators {
	public static void main(String[] args) {
		
		int A =10;
		int B= 20;
		
		System.out.println(A<B);  //T
		System.out.println(A>B);//F
		System.out.println(A<=B);//T
		System.out.println(A>=B);//F
		System.out.println(A==B);//F
		System.out.println(A!=B);//T
		
	
	}

}
