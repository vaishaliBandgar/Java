package Loopstatement;

public class whileloop6 {
	public static void main(String[] args) {
		
		//WAP Print 1 to 30 odd number using while loop
		
		int number =1;
		while (number<=30) {
			System.out.println(number);
			number= number+2;
			
		}
		System.out.println("loop completed");
		
	}

}
