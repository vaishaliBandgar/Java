package Loopstatement;

public class forloop3 {
	public static void main(String[] args) {
		
		
		//wap to print 5 to 20 numbers using for loop
		//starting cond-5
		//end condition-20
		//1++
		//5<=20-true
		//20<=20-true
		//21<=20-false
		for (int a=5;a<=20;a++) {

			System.out.println(a);
		}
		
		System.out.println("Loop completed");



	}

}
