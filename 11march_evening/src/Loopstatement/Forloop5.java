package Loopstatement;

public class Forloop5 {
	
	public static void main(String[] args) {
		
		//output
		
	//	starting condition-1
	//Ending condition-20
		//inc/dec i=i+2;
		
		for (int i=1;i<=20;i=i+2) {
			System.out.println(i);
		}
		System.out.println("loop completed");
		
	}

}
