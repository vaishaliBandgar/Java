package Loopstatement;

public class forloop7 {
	public static void main(String[] args) {
		
	
	
	//Addition of first 10 numbers
	
	// output-55
	
	int sum=0;
	for (int i=1;i<=10;i++) {
		
		sum=sum+i;
		
	}
	System.out.println("addition of first 10 numbers :" +sum);
	
	
	}
	
	

}
